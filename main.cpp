void moverJugador(int direccion){
    switch(direccion){
        case MOV_ARRIBA://Arriba
            if(esValido(posX,posZ-1) && mapa[posZ-1][posX]!=1){
                for (int i = 0; i < tamCubo; i++)
                {
                    zActual-=1;
                    rotx-=4.5;
                    display();
                }
                posZ--;
            }
            break;
        case MOV_ABAJO://Abajo
            if(esValido(posX,posZ+1) && mapa[posZ+1][posX]!=1){
                for (int i = 0; i < tamCubo; i++)
                {
                    zActual+=1;
                    rotx+=4.5;
                    display();
                }
                posZ++;
            }
            break;
        case MOV_IZQUIERDA://Izquierda
            if(esValido(posX-1,posZ) && mapa[posZ][posX-1]!=1){
                for (int i = 0; i < tamCubo; i++){
                    xActual-=1;
                    roty+=4.5;
                    display();
                }
                posX--;
            }
            break;
        case MOV_DERECHA://Derecha
            if(esValido(posX+1,posZ) && mapa[posZ][posX+1]!=1){
                for (int i = 0; i < tamCubo; i++){
                    xActual+=1;
                    roty-=4.5;
                    display();
                }
                posX++;
            }
            break;
    }
}
