bool esValido(int X, int Z) {
    return X>=0 && X<largoMapa && Z>=0 && Z<altoMapa;
}
void crearCamino(int a,int b, int pos){
	//printf("%d %d\n",a,b);
	if (!esValido(a, b)) {
            return;
        }
        if (mapa[a][b] == 3) {
            xx[pos]=b;
            yy[pos]=a;
            xx[pos+1]=-1;
            yy[pos+1]=-1;
            fin =1;
            return;
        }
        if (mapa[a][b] != 0) {
            return ;
        }
        mapa[a][b] = 1;
        for (int d = 0; d < 4; d++) {
			xx[pos]=b;
            yy[pos]=a;
            crearCamino(a + dr[d], b + dc[d],pos+1);
            if(fin) return;
        }
        return ;
}

